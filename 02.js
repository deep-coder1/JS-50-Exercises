// Looping :
// Print the numbers using the loop

// Print the numbers from 1 to 5 to the console using a loop.

console.log("Using a for Loop");

for(let i=1; i<=5; i++){
    console.log(i);
}

// Output:-
// Using a for Loop
// 02.js:9 1
// 02.js:9 2
// 02.js:9 3
// 02.js:9 4
// 02.js:9 5

console.log("Using a while Loop");

let i = 1;
while(i<=5){
    console.log(i);
    i++;
}

// Output:-
// Using a while Loop
// 02.js:24 1
// 02.js:24 2
// 02.js:24 3
// 02.js:24 4
// 02.js:24 5

console.log("Using a do-while Loop");
let j = 1;
do{
    console.log(j);
    j++;
}while(j<=5);

// Output:-
// Using a do-while Loop
// 02.js:39 1
// 02.js:39 2
// 02.js:39 3
// 02.js:39 4
// 02.js:39 5

